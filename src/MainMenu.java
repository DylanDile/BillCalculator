    private void btn_calcBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcBillActionPerformed
        // TODO add your handling code here:
        try {
            if (txt_qty.getText() == "") {
                JOptionPane.showMessageDialog(null, "Quantity is required.", "Error Message.!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String rate = txt_rate.getText().trim();
            String quantity = txt_qty.getText().trim();

            int qty = Integer.parseInt(quantity);
            double rateDouble = Double.parseDouble(rate);
            double bill = qty * rateDouble;

            String billAmnt = Double.toString(bill);
            txt_bill.setText(billAmnt);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error Message.!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_calcBillActionPerformed


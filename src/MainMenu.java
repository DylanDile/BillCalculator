    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String query = "delete from bills where c_id = ? ";
            PreparedStatement psCheck;
            psCheck = MyConnection.getConnection().prepareStatement(query);
            psCheck.setString(1, txt_id.getText());
            psCheck.executeUpdate();
            
            String sql = "INSERT INTO `bills`( `c_id`, `quantity`, `bill`) VALUES (?,?,?)";
            PreparedStatement ps;
            ps = MyConnection.getConnection().prepareStatement(sql);
            ps.setString(1, txt_id.getText());
            ps.setString(2, txt_qty.getText());
            ps.setString(3, txt_bill.getText());

            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Bill added for customer.", "Success Message.!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Failed to add bill!", "Error Message.!", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error Message.!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed
    private void loadTable() {
        try {
            customers.removeAll();
            String sql = "select * from customers";
            PreparedStatement ps;
            ps = MyConnection.getConnection().prepareStatement(sql);

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                ///DefaultTableModel model = (DefaultTableModel) customers.getModel();
                customers.setModel(DbUtils.resultSetToTableModel(rs));
                txt_header.setText("Customers");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error Message.!", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void loadBills() {
        try {
            customers.removeAll();
            String sql = "SELECT * FROM `bills` ";
            PreparedStatement ps;
            ps = MyConnection.getConnection().prepareStatement(sql);

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                ///DefaultTableModel model = (DefaultTableModel) customers.getModel();
                customers.setModel(DbUtils.resultSetToTableModel(rs));
                txt_header.setText("Bills");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error Message.!", JOptionPane.ERROR_MESSAGE);
        }
    }


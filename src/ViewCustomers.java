    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // TODO add your handling code here:
        try {

            String sql;
            if (txt_search.getText().trim() == "") {
                sql = "select * from customers";
            } else {
                sql = "select * from customers where c_name like '%" + txt_search.getText() + "%'  or c_surname like '%" + txt_search.getText() + "%' ";
            }
            PreparedStatement ps;
            ps = MyConnection.getConnection().prepareStatement(sql);

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                ///DefaultTableModel model = (DefaultTableModel) customers.getModel();
                customers.setModel(DbUtils.resultSetToTableModel(rs));
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error Message.!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txt_searchKeyReleased

    private void customersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customersMouseClicked
        // TODO add your handling code here:        
        int i = customers.getSelectedRow();
        TableModel model = customers.getModel();
        txt_id.setText(model.getValueAt(i, 0).toString());
        txt_name.setText(model.getValueAt(i, 1).toString());
        txt_surname.setText(model.getValueAt(i, 2).toString());
        txt_address.setText(model.getValueAt(i, 3).toString());
        txt_phone.setText(model.getValueAt(i, 4).toString());
        txt_sex.setText(model.getValueAt(i, 5).toString());
        txt_dob.setText(model.getValueAt(i, 6).toString());
    }//GEN-LAST:event_customersMouseClicked

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        try {

            String sql = "delete from customers where id = ?";
            PreparedStatement ps;
            ps = MyConnection.getConnection().prepareStatement(sql);
            ps.setString(1, txt_id.getText());
            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Customer details deleted.", "Success Message.!", JOptionPane.INFORMATION_MESSAGE);
                clearFields();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete customer details!", "Error Message.!", JOptionPane.ERROR_MESSAGE);
            }
            loadTable();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error Message.!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

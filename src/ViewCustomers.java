    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {

            String sql = "update customers set c_name = ? , c_surname = ? , c_address = ? , c_phone = ?, c_sex = ? , c_dob = ?  where id = ?";
            PreparedStatement ps;
            ps = MyConnection.getConnection().prepareStatement(sql);
            ps.setString(1, txt_name.getText());
            ps.setString(2, txt_surname.getText());
            ps.setString(3, txt_address.getText());
            ps.setString(4, txt_phone.getText());
            ps.setString(5, txt_sex.getText());
            ps.setString(6, txt_dob.getText());
            ps.setString(7, txt_id.getText());

            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Customer details updated.", "Success Message.!", JOptionPane.INFORMATION_MESSAGE);
                clearFields();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to update customer details!", "Error Message.!", JOptionPane.ERROR_MESSAGE);
            }
            
            loadTable();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error Message.!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_updateActionPerformed

